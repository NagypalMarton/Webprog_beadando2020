<h2>Az Alapítványunkról</h2>
<div id="szoveg">
<p>A magyar háztartások 43%-ában található kutya 27%-ában macska, ez kb 4 millió állattartót jelent. Ennyi 
jóérzésű embert kényszerít felháborodásra a manapság napi szinten előforduló állatkínzás és -bántalmazás, gondatlan 
tartás. Ezek egy jó részét ma már bejelentik, kivizsgálásukra, utóellenőrzésükre nincs kapacitása sem a rendőrségnek, 
sem az önkormányzatoknak.</p>

<p>2011-ben ezt a feladatot vállalta fel az Országos Állatvédőrség Alapítvány, amely egy olyan dinamikusan fejlődő 
állatvédelmi szervezet, amely egy civil szerveződésből országos szervezetté nőtt. Az alapítvány számtalan állami szervvel
 (ORFK,NAV) szerződéses viszonyban áll, együtt dolgozik és napi szinten, országszerte rengeteg eljárásban segíti a 
 rendőrök és a jegyzők munkáját.</p>

 <p>Az Állatvédőrség egyedülálló módon több ezer állatvédelmi jogból és szakmai ismeretekből levizsgázott állatvédőrrel 
 rendelkezik, az ország egész területén és ezen kívül több ezer csatlakozni és segíteni vágyó civilt és együttműködő 
 szervezetet tud felmutatni. Minden megyében egy-egy alegység dolgozik, saját koordinátorral, szerződött állatorvosokkal.</p>

 <p>Az ország minden pontján ellenőrizzük a bejelentéseket, mentjük a bajba jutott, megkínzott állatokat, gyógyítjuk, 
 rehabilitáljuk és elhelyezzük őket. Az állatviadalok áldozatai ugyan úgy számíthatnak a segítségünkre, mint a szaporító
 telepen sínylődő cicák, kutyák, vagy az éheztetett haszonállatok.</p>

 <p>Ugyancsak fontos része a munkánknak oktatási programok szervezése felelős állattartás témában, az óvodásoktól a
 tanítóképzőig, sőt, az ORFK és a NAV szakemberei részére tartunk állatvédelmi vonatkozású előadásokat.</p>

 <p>Mindezek mellett pedig több tucat hazai állatmenhellyel és állatvédő szervezettel állunk munkakapcsolatban és
 közösen harcolunk az állatok jogaiért, érdekeiért a törvényalkotás és a jogalkalmazás szintjén is.</p>
